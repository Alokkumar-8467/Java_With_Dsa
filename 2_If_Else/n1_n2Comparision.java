public class n1_n2Comparision{
  public static void main(String[] args) {
    int n1 = 15;
    int n2 = 20;

    if(n1 == n2){
      System.out.println(n1 + " n1 is equal to n2 " + n2);
    } 
    else if(n1 > n2){
      System.out.println(n1 + " n1 is greater than n2 " + n1);
    }
    else{
      System.out.println(n1 + " n1 is less than n2 " + n2);
    }
  }
}