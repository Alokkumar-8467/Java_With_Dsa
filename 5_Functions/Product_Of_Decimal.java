public class Product_Of_Decimal {

  public static double pro(double x, double y) {
    double products = x * y;
    return products;
  }

  public static void main(String args[]) {
    double a = 12.5;
    double b = 12.5;

    double result = pro(a, b);
    System.out.println(result);

  }

}
